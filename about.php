          <h2>What is Stats of Origin</h2>
          <p><i>Stats of Origin</i> was created in July of 2014 due to an argument over State of Origin. It was originally called <i>Origin Stats</i> but changed name the following year.</p>
          <h3>What is State of Origin</h3>
          <p>If you're in Australia, you'd have to live under a rock to not know about this great rivalry... for those living under rocks and everyone else around the world, State of Origin is the annual Rugby League play off between the states of New South Wales and Queensland. A best of three showdown, the competition is often the highest caliber of Rugby League to be played anwyhere around the world in that year, even if it happens to be a World Cup year. This is quite a feat, since the teams comprise of players from only two of Australia's states.</p>
          <h3>Player eligibility</h3>
          <p>The selection criteria is simple; a player can only represent their state if their first senior representative game was played for that state. This has been an issue of some contention, with greats such as Greg Inglis who ended up playing for Qld but actually represented NSW first!</p>
          <h3>An argument you say</h3>
          <p>Fans and players alike get massively emotional over this clash. Very often they forget how close the games have been over time, and always seem to forget the overall historical tally. This website is to demonstrate that history, a history of intense football between two great states.</p>
